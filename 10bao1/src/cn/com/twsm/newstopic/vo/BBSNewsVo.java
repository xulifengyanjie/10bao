package cn.com.twsm.newstopic.vo;

public class BBSNewsVo {
	private String id;
	private String title;
	private String weight;
	private String hitsCount;
	private String collectTime;
	private String subType;
	private String poster;
	private String commentsCount;
	private String smallType;
	private String forumLink;
	private String collectSource;
	private String bigType;
	private String postTime;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getWeight() {
		return weight;
	}

	public void setWeight(String weight) {
		this.weight = weight;
	}

	public String getHitsCount() {
		return hitsCount;
	}

	public void setHitsCount(String hitsCount) {
		this.hitsCount = hitsCount;
	}

	public String getCollectTime() {
		return collectTime;
	}

	public void setCollectTime(String collectTime) {
		this.collectTime = collectTime;
	}

	public String getSubType() {
		return subType;
	}

	public void setSubType(String subType) {
		this.subType = subType;
	}

	public String getPoster() {
		return poster;
	}

	public void setPoster(String poster) {
		this.poster = poster;
	}

	public String getCommentsCount() {
		return commentsCount;
	}

	public void setCommentsCount(String commentsCount) {
		this.commentsCount = commentsCount;
	}

	public String getSmallType() {
		return smallType;
	}

	public void setSmallType(String smallType) {
		this.smallType = smallType;
	}

	public String getForumLink() {
		return forumLink;
	}

	public void setForumLink(String forumLink) {
		this.forumLink = forumLink;
	}

	public String getCollectSource() {
		return collectSource;
	}

	public void setCollectSource(String collectSource) {
		this.collectSource = collectSource;
	}

	public String getBigType() {
		return bigType;
	}

	public void setBigType(String bigType) {
		this.bigType = bigType;
	}

	public String getPostTime() {
		return postTime;
	}

	public void setPostTime(String postTime) {
		this.postTime = postTime;
	}

}
