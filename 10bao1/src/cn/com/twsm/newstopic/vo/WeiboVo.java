package cn.com.twsm.newstopic.vo;

public class WeiboVo {
	private String text;
	private String weight;
	private String repostsCount;
	private String source;
	private String createdAt;
	private String link;
	private String commentsCount;
	private String repostsId;
	private String weiboId;
	private String publisher;
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getRepostsCount() {
		return repostsCount;
	}
	public void setRepostsCount(String repostsCount) {
		this.repostsCount = repostsCount;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getCreatedAt() {
		return createdAt;
	}
	public void setCreatedAt(String createdAt) {
		this.createdAt = createdAt;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getCommentsCount() {
		return commentsCount;
	}
	public void setCommentsCount(String commentsCount) {
		this.commentsCount = commentsCount;
	}
	public String getRepostsId() {
		return repostsId;
	}
	public void setRepostsId(String repostsId) {
		this.repostsId = repostsId;
	}
	public String getWeiboId() {
		return weiboId;
	}
	public void setWeiboId(String weiboId) {
		this.weiboId = weiboId;
	}
	public String getPublisher() {
		return publisher;
	}
	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}
	
}
